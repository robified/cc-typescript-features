const add = (a: number, b: number) => {
    return a + b;
};

const subtract = (a: number, b: number): number => {
    return a - b;
};

function divide(a: number, b: number): number {
    return a / b;
}

// anonymouse function
const multiple = function(a: number, b: number): number {
    return a * b;
};
